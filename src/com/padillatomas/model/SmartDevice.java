package com.padillatomas.model;

public class SmartDevice {
//     Atributos:
    public String marca;
    public String modelo;
    public String memoria;
    public String ram;

//    Constructor:
    public SmartDevice(String marca, String modelo, String memoria, String ram) {
        this.marca = marca;
        this.modelo = modelo;
        this.memoria = memoria;
        this.ram = ram;
    }
}
